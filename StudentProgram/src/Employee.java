
public class Employee {
	int empId;
	String name;
	double salary;

	double getSalary()
	{
		return salary;
	}
	
	void setSalary (double salary)
	{
		this.salary=salary;
	}
	
	int getEmpId()
	{
		return empId;
	}
	
	void setEmpId (int empId)
	{
		this.empId=empId;
	}
	
	String getName()
	{
		return name;
	}
	
	void setName (String name)
	{
		this.name=name;
	}
	
}
